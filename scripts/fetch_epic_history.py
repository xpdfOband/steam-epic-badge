def generate_epic_history_json():
    """生成完整的 Epic 赠送历史 JSON"""
    games = []

    for game in EPIC_FREE_GAMES_HISTORY:
        title = game["title"]
        steam_appid = game.get("steam_appid") or get_steam_appid(title)

        # 生成 epic_id (使用小写标题，替换空格为连字符)
        epic_id = title.lower().replace(" ", "-").replace("'", "").replace(":", "").replace(".", "")

        games.append({
            "title": title,
            "epic_id": epic_id,
            "steam_appid": steam_appid,
            "free_dates": [
                {
                    "start": game["start"],
                    "end": game["end"],
                    "type": "giveaway"
                }
            ],
            "image": f"https://cdn.akamai.steamstatic.com/steam/apps/{steam_appid}/header.jpg" if steam_appid else None
        })

    return {
        "games": games,
        "last_updated": datetime.now().strftime("%Y-%m-%d")
    }

